import Foundation
import UIKit

extension UIColor {
    @nonobjc class var wtGreen: UIColor {
        return UIColor(red: 69.0 / 255.0, green: 139.0 / 255.0, blue: 68.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var wtPurple: UIColor {
      return UIColor(red: 129.0 / 255.0, green: 40.0 / 255.0, blue: 131.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var wtYellow: UIColor {
      return UIColor(red: 254.0 / 255.0, green: 224.0 / 255.0, blue: 117.0 / 255.0, alpha: 1.0)
    }
}

